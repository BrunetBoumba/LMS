         String date_em = jTxt_Date_emprunt.getText();
        String date_ret = jTxt_Date_remise.getText();
        String id_item = jTxt_code_livre.getText();
        String matricule = jTxT_mat.getText();
        String nb = jCmb_Nbre.getSelectedItem().toString();
        
         int vnb = Integer.valueOf(nb);
        int id_trqnsaction = 0;
        
        
       
            String strDateMysql = GestionDates.chaineDateFRversEN(date_em);
            String strDateMysql1 = GestionDates.chaineDateFRversEN(date_ret);
            Date dtDate = new Date();
            dtDate = GestionDates.dateJavaEnDateSQL(dtDate);
             Date dtDate1 = new Date();
            dtDate1 = GestionDates.dateJavaEnDateSQL(dtDate1);
            Transaction 
             l_Transaction = new Transaction(dtDate, dtDate1, id_item, matricule,id_trqnsaction, vnb);
            boolean breation = false;
            breation = l_Transaction.lireRecupCRUD(matricule, id_item);
             if (!matricule.equals("") && breation == true ) {
            // Sauvegarde dans la BD
           
            boolean bCreation = false;
             try {
                 bCreation = l_Transaction.creerCRUD(strDateMysql, strDateMysql1, id_item, matricule,id_trqnsaction, vnb);
             } catch (SQLException ex) {
                 Logger.getLogger(EmpruntArticles.class.getName()).log(Level.SEVERE, null, ex);
             }
            if (bCreation) {
               
               
              
            }   
             jTxT_mat.setText("");
              jTxt_Date_emprunt.setText("");
              jTxt_Date_remise.setText("");
              jTxt_code_livre.setText("");
             
                dtDate = new Date();
              jTxt_Date_emprunt.setText(GestionDates.dateEnChaineFR(dtDate));
               dtDate = new Date();
              jTxt_Date_remise.setText(GestionDates.dateEnChaineFR(dtDate));
               
              
             }else {
                 JOptionPane.showMessageDialog(null, "La saisie du matricule" + " est obligatoire", "V√©rifier votre saisie", JOptionPane.ERROR_MESSAGE );
             }
           
                                        
